#include<stdio.h>
int main()
{
   // \b
   printf("my\b name\b is\b anurag\b \n");

   // \t
   printf("my\t name\t is\t anurag\n");

   // \r
   printf("hi my\r \n");

   // \f
  printf("my\f name\f is\f dash\n");
   
   // \a
  printf("my\a name\a is\a dash\a\n");

   // \v
   printf("my\v name\v is\v dash\v\n");

return 0;
}
